def render_entry(entry, entries_by_id, sources_by_id):
    claim_map = {claim["id"]: claim for claim in entry["claims"]}
    used_source_ids = []
    for claim in entry["claims"]:
        for source_id in claim["source_ids"]:
            if source_id not in used_source_ids:
                used_source_ids.append(source_id)
    for comparison in entry.get("comparisons", []):
        for source_id in comparison["source_ids"]:
            if source_id not in used_source_ids:
                used_source_ids.append(source_id)
    source_numbers = {source_id: index + 1 for index, source_id in enumerate(used_source_ids)}

    sections = []
    for section in entry["sections"]:
        paragraphs = []
        for paragraph in section["paragraphs"]:
            source_ids = source_ids_for_claims(paragraph["claim_ids"], claim_map)
            citations = citation_links(source_ids, source_numbers)
            paragraphs.append(f'<p>{esc(paragraph["text"])} <span class="citations">{citations}</span></p>')
        sections.append(
            f'<section class="lore-section"><h2>{esc(section["heading"])}</h2>{"".join(paragraphs)}</section>'
        )

    comparison_rows = []
    for comparison in entry.get("comparisons", []):
        citations = citation_links(comparison["source_ids"], source_numbers)
        comparison_rows.append(
            "<tr>"
            f'<th scope="row">{esc(comparison["topic"])}</th>'
            f'<td>{esc(comparison["real_world"])}</td>'
            f'<td>{esc(comparison["division"])}</td>'
            f'<td>{esc(comparison["assessment"])} <span class="citations">{citations}</span></td>'
            "</tr>"
        )
    comparison_block = ""
    if comparison_rows:
        comparison_block = f'''<section class="lore-section">
  <h2>Real world vs. The Division</h2>
  <div class="table-scroll"><table class="comparison-table">
    <thead><tr><th>Topic</th><th>Real world</th><th>The Division</th><th>Assessment</th></tr></thead>
    <tbody>{''.join(comparison_rows)}</tbody>
  </table></div>
</section>'''

    claims = []
    for claim in entry["claims"]:
        citations = citation_links(claim["source_ids"], source_numbers)
        claims.append(
            f'<li><span class="claim-status">{esc(claim["assessment"].replace("-", " "))}</span>'
            f'<p>{esc(claim["text"])} <span class="citations">{citations}</span></p></li>'
        )

    related = []
    for relation in entry.get("relations", []):
        target = entries_by_id[relation["target_id"]]
        related.append(
            f'<li><a href="{page_url(target)}">{esc(target["title"])}</a>'
            f'<span>{esc(relation["relationship"].replace("-", " "))}</span></li>'
        )
    related_block = ""
    if related:
        related_block = f'<section class="lore-section"><h2>Related records</h2><ul class="related-list">{"".join(related)}</ul></section>'

    sources = []
    for source_id in used_source_ids:
        source = sources_by_id[source_id]
        number = source_numbers[source_id]
        publication = f' — {esc(source["publisher"])}'
        date = f' ({esc(source.get("published_date", "undated"))})'
        sources.append(
            f'<li id="source-{number}"><span class="source-number">[{number}]</span> '
            f'<a href="{esc(source["url"])}">{esc(source["title"])}</a>{publication}{date}. '
            f'<span>Accessed {esc(source["accessed_date"])}.</span></li>'
        )

    canonical_path = page_url(entry)
    canonical = BASE_URL + canonical_path
    verification = entry["verification"]
    aliases = ""
    if entry.get("aliases"):
        aliases = f'<p class="aliases"><strong>Also known as:</strong> {esc(", ".join(entry["aliases"]))}</p>'
    content = f'''{head(entry['title'], entry['summary'], canonical)}
<body>
{header()}
<main class="lore-shell">
  <nav class="breadcrumbs" aria-label="Breadcrumb"><a href="/">Home</a> / <a href="/lore/">Lore</a> / <a href="/lore/{esc(entry['section'])}/">{esc(section_label(entry['section']))}</a> / <span>{esc(entry['title'])}</span></nav>
  <article class="lore-article">
    <header class="lore-hero">
      <p class="eyebrow">{esc(entry['type'].replace('-', ' '))}</p>
      <h1>{esc(entry['title'])}</h1>
      <p class="lede">{esc(entry['summary'])}</p>
      {aliases}
      <div class="evidence-strip">
        <span><strong>Connection</strong>{esc(entry['connection_status'].replace('-', ' '))}</span>
        <span><strong>Continuity</strong>{esc(entry['continuity'].replace('-', ' '))}</span>
        <span><strong>Canon scope</strong>{esc(entry['canon_status'].replace('-', ' '))}</span>
        <span><strong>Verification</strong>{esc(verification['status'].replace('-', ' '))}</span>
        <span><strong>Reviewed</strong>{esc(verification['last_reviewed'])}</span>
      </div>
    </header>
    {''.join(sections)}
    {comparison_block}
    <section class="lore-section">
      <h2>Claim ledger</h2>
      <p class="section-note">Every factual statement below must retain at least one traceable source.</p>
      <ol class="claim-ledger">{''.join(claims)}</ol>
    </section>
    {related_block}
    <section class="lore-section source-section">
      <h2>Sources</h2>
      <ol class="source-list">{''.join(sources)}</ol>
    </section>
    <aside class="review-note"><strong>Editorial status:</strong> {esc(verification.get('notes', ''))}</aside>
  </article>
</main>
{footer()}
</body>
</html>
'''
    return "\n".join(line.rstrip() for line in content.splitlines()) + "\n"


