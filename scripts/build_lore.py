                f'<td>{category["human_reviewed_count"]}</td>'
                f'<td>{category["published_count"]}</td>'
                f'<td><span class="coverage-status">{esc(category["status"].replace("-", " "))}</span><small>{esc(category["notes"])}</small></td>'
